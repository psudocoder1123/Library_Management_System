public static boolean validateUserName(String str){
 

    return ((!str.equals("")) 
            && (str != null) 
            && (str.matches("^[a-z A-Z]*$")));
}
public static boolean validateID3digit(String str){
 

    return ((!str.equals("")) 
            && (str != null) 
            && (str.matches("^\\d{1,3}$")));
}
public static boolean validate_phoneno(String str){
 

    return ((!str.equals("")) 
            && (str != null) 
            && (str.matches("^\\d{3}\\d{3}\\d{4}$")));
}
public static boolean bookIdValidate(String str){
 

    return ((!str.equals("")) 
            && (str != null) 
            && (str.matches("^(?=.*\\d).{2,6}$")));
}
