public static boolean validateUserName(String str){
 

    return ((!str.equals("")) 
            && (str != null) 
            && (str.matches("^[a-zA-Z]*$")));
}
public static boolean validate_phoneno(String str){
 

    return ((!str.equals("")) 
            && (str != null) 
            && (str.matches("^\\d{3}\\d{3}\\d{4}$")));
}
public static boolean rollValidate(String str){
 

    return ((!str.equals("")) 
            && (str != null) 
            && (str.matches("^(?=.*\\d).{2,6}$")));
}
public static boolean validate_email(String str){
 

    return ((!str.equals("")) 
            && (str != null) 
            && (str.matches("^[A-Za-z0-9](([_\\.\\-]?[a-zA-Z0-9]+)*)@([A-Za-z0-9]+)(([\\.\\-]?[a-zA-Z0-9]+)*)\\.([A-Za-z]{2,})$")));
}
