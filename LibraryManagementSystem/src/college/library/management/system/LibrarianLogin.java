    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql="select * from data.user_data where name = '"+usernameTxtfield.getText()+"'and password='"+jPasswordField1.getText()+"' ";
        
        try {
            ps= conn.prepareStatement(sql);
        } catch (SQLException ex) {
            Logger.getLogger(LibrarianLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            rs=ps.executeQuery();
        } catch (SQLException ex) {
            Logger.getLogger(LibrarianLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            if(rs.next())
            {
                JOptionPane.showMessageDialog(null,"Login Sucessful");
                LibrarianPannel LP=new LibrarianPannel();
                setVisible(false);
                LP.setVisible(true);
            }
            
            else
            {
                JOptionPane.showMessageDialog(null,"Login UnSucessful");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LibrarianLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

